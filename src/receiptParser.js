// 한 글자/짧은 별칭이 다른 단어 속에 끼는 것 방지 (예: "무"가 "풀무원"에)
function hasIngredientKeyword(line, alias) {
  if (/[\s(),0-9]/.test(alias)) return line.includes(alias);
  if (alias.length >= 2) return line.includes(alias);
  const idx = line.indexOf(alias);
  if (idx === -1) return false;
  const before = idx === 0 ? "" : line[idx - 1];
  const after = idx + alias.length >= line.length ? "" : line[idx + alias.length];
  const isKorean = (ch) => /[가-힣]/.test(ch);
  return !isKorean(before) && !isKorean(after);
}

export function parseReceiptText(rawText) {
  const lines = rawText.split(/[\n,]/).map(l => l.trim()).filter(Boolean);
  const found = new Set();
  const ignoredHits = [];
  for (const line of lines) {
    if (RECEIPT_IGNORE.some(ig => line.includes(ig))) { ignoredHits.push(line); continue; }
    for (const [ingredient, aliases] of Object.entries(RECEIPT_INGREDIENT_MAP)) {
      if (aliases.some(a => hasIngredientKeyword(line, a))) found.add(ingredient);
    }
  }
  return { matched: [...found], ignored: ignoredHits };
}
